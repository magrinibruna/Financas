package model;

public class Configuracao {
	
	private String usuario;
	private double bruto;
	private double liquido;
	private double aposentadoria;
	private double reserva;
	
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public double getBruto() {
		return bruto;
	}
	public void setBruto(double bruto) {
		this.bruto = bruto;
	}
	public double getLiquido() {
		return liquido;
	}
	public void setLiquido(double liquido) {
		this.liquido = liquido;
	}
	public double getAposentadoria() {
		return aposentadoria;
	}
	public void setAposentadoria(double aposentadoria) {
		this.aposentadoria = aposentadoria;
	}
	public double getReserva() {
		return reserva;
	}
	public void setReserva(double reserva) {
		this.reserva = reserva;
	}

	
	
	
	
}
